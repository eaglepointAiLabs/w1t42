const ORDER_STATES = {
  CREATED: "created",
  PENDING_PAYMENT: "pending_payment",
  PAID: "paid",
  COMPLETED: "completed",
  CANCELLED: "cancelled",
  REFUND_PARTIAL: "refund_partial",
  REFUND_FULL: "refund_full"
};

const ALLOWED_TRANSITIONS = {
  [ORDER_STATES.CREATED]: [ORDER_STATES.PENDING_PAYMENT, ORDER_STATES.CANCELLED],
  [ORDER_STATES.PENDING_PAYMENT]: [ORDER_STATES.PAID, ORDER_STATES.CANCELLED],
  [ORDER_STATES.PAID]: [ORDER_STATES.COMPLETED, ORDER_STATES.REFUND_PARTIAL, ORDER_STATES.REFUND_FULL],
  [ORDER_STATES.COMPLETED]: [ORDER_STATES.REFUND_PARTIAL, ORDER_STATES.REFUND_FULL],
  [ORDER_STATES.CANCELLED]: [],
  [ORDER_STATES.REFUND_PARTIAL]: [ORDER_STATES.REFUND_FULL],
  [ORDER_STATES.REFUND_FULL]: []
};

function canTransition(fromState, toState) {
  return (ALLOWED_TRANSITIONS[fromState] || []).includes(toState);
}

module.exports = {
  ORDER_STATES,
  canTransition
};
